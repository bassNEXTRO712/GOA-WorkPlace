#) შექმენით ფუნქცია manual_pop, რომელსაც გადაეცემა ორი არგუმენტი, სია და ინდექსი. წაშალეთ სიიდან,
#  გადმოცემულ ინდექსზე მყოფი ელემენტი. საბოლოოდ კი დააბრუნეთ ეს სია(არ გამოიყენოთ .pop ფუნქცია)
def manual_pop(lst, index):
    new_list = []
    for i in range(len(lst)):
        if i != index:
            new_list.append(lst[i])
    
    return new_list
print(manual_pop([13242345235, 2232223233, 33334444, 43234345555, 23234445,232323,2324342,24323132,2143124], 2))